<h4>About the United States of America:</h4>
<p>The <span class="b">United States of America</span> — also referred to as 'the United States', 'the US', 'the USA', 'America' (more loosely), 'the States' (colloquially), and 'Columbia' (poetically) — is a federal republic of 50 states with 1 Federal District and 14 other Insular Areas/ Possessions, located primarily in central North America. </p>

<p>In the United States, education is a state, not federal, responsibility, and the laws and standards vary considerably. After high school (twelfth), students have a choice of attending either a public/state university, a private university, entering the workforce, or enlisting in the military. Public universities receive funding from the federal and state government but students still pay tuition, which can vary depending on the university, state, and whether the student is a resident of the state or not. Tuition at private universities tends to be much higher than at public universities. For more information, visit <a href="http://en.wikipedia.org/wiki/United_States" target="_blank"><b>Wikipedia</b></a></p>

